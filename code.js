let num1 = Number(prompt("input your first number:"));
let num2 = Number(prompt("input your second number:"));
let opr = prompt("what operation do you want to perform e.g +,-,*,/,");
if(opr=="+"){
   alert(num1 + num2)
}
else if (opr == "-") {
  alert(num1 - num2)
}
else if (opr == "*"){
  alert(num1*num2)
}
else if (opr == "/") {
  alert(num1 / num2)
}